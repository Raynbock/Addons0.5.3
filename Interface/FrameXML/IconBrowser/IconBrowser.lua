-- Working list (filtered or full); by default it's the full list.
IconBrowser_Icons = IconBrowser_AllIcons;
IconBrowser_FilterText = "";

ICONBROWSER_ICONS_PER_PAGE = 24;

function IconBrowser_OnLoad()
    this.page = 1;
end

local function IconBrowser_GetNumIcons()
    return table.getn(IconBrowser_Icons);
end

function IconBrowser_ApplyFilter()
    local filter = IconBrowser_FilterText;
    if ( not filter or filter == "" ) then
        IconBrowser_Icons = IconBrowser_AllIcons;
    else
        local lowerFilter = strlower(filter);
        local result = {};
        local count = 0;
        for index = 1, table.getn(IconBrowser_AllIcons) do
            local path = IconBrowser_AllIcons[index];
            if ( strfind(strlower(path), lowerFilter, 1, 1) ) then
                count = count + 1;
                result[count] = path;
            end
        end
        IconBrowser_Icons = result;
    end
    if ( IconBrowserFrame ) then
        IconBrowserFrame.page = 1;
    end
    IconBrowser_Update();
end

function IconBrowser_OnFilterChanged()
    if ( IconBrowserFilterBox ) then
        IconBrowser_FilterText = IconBrowserFilterBox:GetText();
        IconBrowser_ApplyFilter();
    end
end

function IconBrowser_OnFilterEscape()
    if ( IconBrowserFilterBox ) then
        IconBrowserFilterBox:ClearFocus();
    end
end

function IconBrowser_OnMouseDown()
    -- Clicking anywhere on the icon browser should leave the search focus.
    if ( IconBrowserFilterBox ) then
        IconBrowserFilterBox:ClearFocus();
    end
    -- 0.5.3 frames don't universally support moving helpers; keep this safe.
    if ( this and this.StartMoving ) then
        this:StartMoving();
    elseif ( this and this.StartMovingOrSizing ) then
        this:StartMovingOrSizing();
    end
end

function IconBrowser_OnMouseUp()
    if ( this and this.StopMovingOrSizing ) then
        this:StopMovingOrSizing();
    end
end

function IconBrowser_Toggle()
    if ( IconBrowserFrame:IsVisible() ) then
        IconBrowserFrame:Hide();
    else
        if ( not IconBrowserFrame.page ) then
            IconBrowserFrame.page = 1;
        end
        IconBrowser_Update();
        IconBrowserFrame:Show();
    end
end

