function IconBrowser_Update()
    local numIcons = IconBrowser_GetNumIcons();
    local page = IconBrowserFrame.page or 1;
    local perPage = ICONBROWSER_ICONS_PER_PAGE;

    local maxPages = 1;
    if ( numIcons > perPage ) then
        maxPages = floor((numIcons - 1) / perPage) + 1;
    end

    if ( page < 1 ) then
        page = 1;
    elseif ( page > maxPages ) then
        page = maxPages;
    end
    IconBrowserFrame.page = page;

    local startIndex = (page - 1) * perPage + 1;

    for i = 1, perPage do
        local button = getglobal("IconBrowserButton"..i);
        local iconIndex = startIndex + i - 1;
        if ( iconIndex <= numIcons ) then
            local texPath = IconBrowser_Icons[iconIndex];
            button.iconIndex = iconIndex;
            local tex = getglobal(button:GetName().."Icon");
            if ( tex ) then
                tex:SetTexture(texPath);
            end
            button:Show();
        else
            button.iconIndex = nil;
            button:Hide();
        end
    end

    if ( IconBrowserPageText ) then
        IconBrowserPageText:SetText(page.." / "..maxPages);
    end

    if ( IconBrowserPrevButton ) then
        if ( page <= 1 ) then
            IconBrowserPrevButton:Disable();
        else
            IconBrowserPrevButton:Enable();
        end
    end
    if ( IconBrowserNextButton ) then
        if ( page >= maxPages ) then
            IconBrowserNextButton:Disable();
        else
            IconBrowserNextButton:Enable();
        end
    end
end

function IconBrowser_NextPage()
    IconBrowserFrame.page = (IconBrowserFrame.page or 1) + 1;
    IconBrowser_Update();
end

function IconBrowser_PrevPage()
    IconBrowserFrame.page = (IconBrowserFrame.page or 1) - 1;
    IconBrowser_Update();
end

local function IconBrowser_GetEscapedPath(index)
    local path = IconBrowser_Icons[index];
    if ( not path ) then
        return nil;
    end
    -- add an extra backslash for each existing one
    local escaped = gsub(path, "\\", "\\\\");
    return escaped;
end

function IconBrowser_IconButton_OnClick()
    if ( not this.iconIndex ) then
        return;
    end
    local escapedPath = IconBrowser_GetEscapedPath(this.iconIndex);
    if ( not escapedPath ) then
        return;
    end
    if ( ChatFrameEditBox ) then
        ChatFrameEditBox:SetText(escapedPath);
        ChatFrameEditBox:Show();
        ChatFrameEditBox:SetFocus();
    elseif ( DEFAULT_CHAT_FRAME ) then
        DEFAULT_CHAT_FRAME:AddMessage(escapedPath);
    end
end

function IconBrowser_IconButton_OnEnter()
    if ( not this.iconIndex ) then
        return;
    end
    local texPath = IconBrowser_Icons[this.iconIndex];
    if ( GameTooltip ) then
        GameTooltip:SetOwner(this, "ANCHOR_RIGHT");
        GameTooltip:SetText(texPath);
    end
end

function IconBrowser_IconButton_OnLeave()
    if ( GameTooltip ) then
        GameTooltip:Hide();
    end
end
